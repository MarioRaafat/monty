#include "monty.h"

/**
* f_add - function that adds the top two elements of the stack
* @head: double pointer head to the stack
* @counter: line count
*
* Return: nothing
*/
void f_add(stack_t **head, unsigned int counter)
{
	stack_t *h;
	int len = 0, temp;

	h = *head;
	while (h)
	{
		h = h->next;
		len++;
	}
	if (len < 2)
	{
		fprintf(stderr, "L%d: can't add, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	temp = h->n + h->next->n;
	h->next->n = temp;
	*head = h->next;
	free(h);
}

/**
* f_sub - function that substracts nodes
* @head: double head pointer to the stack
* @counter: line count
*
* Return: nothing
*/
void f_sub(stack_t **head, unsigned int counter)
{
	stack_t *tmp;
	int sub, ned;

	tmp = *head;
	for (ned = 0; tmp != NULL; ned++)
		tmp = tmp->next;
	if (ned < 2)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	tmp = *head;
	sub = tmp->next->n - tmp->n;
	tmp->next->n = sub;
	*head = tmp->next;
	free(tmp);
}

